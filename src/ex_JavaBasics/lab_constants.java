package ex_JavaBasics;

public class lab_constants {
    public static void main(String[] args) {
        final int a = 10;
        //not possible to change value a because of "final" set the variable as constant
        //primitive variables
        int num = 2;
        byte b = 10;
        short s = 10;
        long l = 987654327;
        float f = 3.14f;
        double d = 67.85236977;
        boolean b1 = true;
        char c = 'A';
        //multiple variable declaration
        int aa = 4, bb = 5, cc = 7;
    }
}
